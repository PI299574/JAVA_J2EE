package io.pintu.junit;

public class MathUtil {

	public int add(int a, int b) {
		return a + b;
	}

	public int divide(int a, int b) {
		return a / b;
	}

	public double computeCircleArea(double r) {
		return Math.PI * r * r;
	}

	public int multiply(int a, int b) {
		return a * b;
	}
}
